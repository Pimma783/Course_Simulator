const courses = rawData.split('\n').filter(Boolean).map(line => {
  const parts = line.split(' ');
  const semester = parseInt(parts[0], 10);
  const courseCode = parts[1];
  
  // Find the credits breakdown, e.g., "3 (2-2-5)" or "1 (0-3-1)"
  const creditsIndex = parts.findIndex(p => p.match(/^\d$/) && parts[parts.indexOf(p)+1] && parts[parts.indexOf(p)+1].startsWith('('));
  
  const courseName = parts.slice(2, creditsIndex).join(' ');
  const totalCredits = parseInt(parts[creditsIndex], 10);
  const creditsBreakdown = parts[creditsIndex + 1].replace(/[()]/g, '');
  
  let categoryStr = parts.slice(creditsIndex + 2).join(' ');
  
  let category = 'major_elective';
  if (categoryStr.includes('หมวดศึกษาทั่วไป')) category = 'general_education';
  if (categoryStr.includes('วิชาเอกบังคับ')) category = 'major_required';
  if (categoryStr.includes('วิชาเอกเลือก')) category = 'major_elective';
  if (categoryStr.includes('วิชาเลือกเสรี')) category = 'free_elective';
  
  const isPlaceholderSlot = categoryStr.includes('Placeholder');
  const choiceGroupMatch = categoryStr.match(/กลุ่ม: (year4-term2-capstone)/);
  const choiceGroupId = choiceGroupMatch ? choiceGroupMatch[1] : null;

  return {
    courseCode,
    courseName,
    totalCredits,
    creditsBreakdown,
    semester,
    category,
    isPlaceholderSlot,
    choiceGroupId,
    prerequisites: []
  };
});

